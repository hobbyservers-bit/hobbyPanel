/**
 * Wings simulator — full implementation.
 *
 * In-process mock of the Wings daemon. Activated when MOCK_WINGS=true.
 * Provides:
 *   - Realistic Paper 1.21.4 boot sequence with proper timing
 *   - State machine: offline → starting → online → stopping → offline
 *   - CPU/RAM stats that drift realistically (spikes on startup, drifts when online)
 *   - Console event bus (any subscriber gets every emitted line)
 *   - 1000-line ring buffer for log replay on "request logs"
 *   - Command handling: list, help, tps, say, time, weather, stop, etc.
 *   - In-memory virtual file system pre-seeded with realistic server files
 *   - Backup simulation
 */

