// ── Stat tick ─────────────────────────────────────────────────────────────────

function startTick(uuid: string) {
  const s = getServer(uuid);
  if (s.tickInterval) return;

  s.tickInterval = setInterval(() => {
    const srv = getServer(uuid);
    if (srv.status === "offline") {
      clearInterval(srv.tickInterval!);
      srv.tickInterval = null;
      return;
    }
    if (srv.status === "running") {
      // CPU: drifts between 8–35 %, occasional spike to 60 %
      const spike = Math.random() < 0.05;
      srv.cpu = spike
        ? 40 + Math.random() * 20
        : Math.max(8, Math.min(35, srv.cpu + (Math.random() - 0.48) * 4));
      // RAM: slowly climbs from 512 MB, cap at 1.8 GB
      srv.memBytes = Math.min(
        1.8e9,
        Math.max(
          256e6,
          srv.memBytes + (Math.random() - 0.3) * 8e6
        )
      );
      // Network: small random deltas
      srv.netRx += Math.round(Math.random() * 4096);
      srv.netTx += Math.round(Math.random() * 2048);
    }
  }, 1000);
}

// ── Power actions ─────────────────────────────────────────────────────────────

async function doStart(uuid: string) {
  const s = getServer(uuid);
  if (s.status !== "offline") return;

  s.status = "starting";
  s.cpu = 5;
  s.memBytes = 128e6;

  startTick(uuid);

  // Replay boot sequence with proper delays
  const base = Date.now();
  for (const [delayMs, line] of BOOT_SEQUENCE) {
    const wait = delayMs - (Date.now() - base);
    if (wait > 0) await delay(wait);
    pushLog(uuid, line);

    // Ramp CPU and RAM as the server "loads"
    const progress = delayMs / 4500;
    s.cpu = 5 + progress * 75 * (0.8 + Math.random() * 0.4);
    s.memBytes = 128e6 + progress * 384e6;
  }

  s.status = "running";
  s.startedAt = Date.now();
  s.cpu = 15 + Math.random() * 10;
  s.memBytes = 512e6 + Math.random() * 64e6;
}

async function doStop(uuid: string, kill = false) {
  const s = getServer(uuid);
  if (s.status === "offline") return;

  s.status = "stopping";

  if (kill) {
    await delay(300);
    pushLog(uuid, `${ts()} INFO]: Server killed`);
  } else {
    const base = Date.now();
    for (const [delayMs, line] of STOP_SEQUENCE) {
      const wait = delayMs - (Date.now() - base);
      if (wait > 0) await delay(wait);
      pushLog(uuid, line);
    }
  }

  s.status = "offline";
  s.cpu = 0;
  s.memBytes = 0;
  s.netRx = 0;
  s.netTx = 0;
  s.startedAt = null;
  s.onlinePlayerCount = 0;

  if (s.tickInterval) {
    clearInterval(s.tickInterval);
    s.tickInterval = null;
  }
}

