// ── Types ─────────────────────────────────────────────────────────────────────

type SimStatus = "running" | "offline" | "starting" | "stopping";

interface SimServer {
  status: SimStatus;
  cpu: number;          // percent
  memBytes: number;
  diskBytes: number;
  netRx: number;
  netTx: number;
  startedAt: number | null;
  onlinePlayerCount: number;
  tickInterval: ReturnType<typeof setInterval> | null;
}

// ── Shared state ──────────────────────────────────────────────────────────────
// Anchored to globalThis so Next.js API routes (webpack module context) and
// server.ts (Node native require) share the same Maps — prevents the split
// where power() fires in one instance but onConsole() is registered in another.

type SimGlobal = {
  _simServers: Map<string, SimServer>;
  _simLogBuffers: Map<string, string[]>;
  _simConsoleListeners: Map<string, Set<(line: string) => void>>;
  _simVirtualFs: Map<string, Map<string, string>>;
};

const g = globalThis as typeof globalThis & Partial<SimGlobal>;

const servers: Map<string, SimServer> = g._simServers ?? (g._simServers = new Map());
const logBuffers: Map<string, string[]> = g._simLogBuffers ?? (g._simLogBuffers = new Map());
const consoleListeners: Map<string, Set<(line: string) => void>> = g._simConsoleListeners ?? (g._simConsoleListeners = new Map());
const virtualFs: Map<string, Map<string, string>> = g._simVirtualFs ?? (g._simVirtualFs = new Map());

const LOG_CAP = 1000;

// ── Helpers ───────────────────────────────────────────────────────────────────

function getServer(uuid: string): SimServer {
  if (!servers.has(uuid)) {
    servers.set(uuid, {
      status: "offline",
      cpu: 0,
      memBytes: 0,
      diskBytes: Math.round((1.2 + Math.random() * 0.6) * 1024 * 1024 * 1024),
      netRx: 0,
      netTx: 0,
      startedAt: null,
      onlinePlayerCount: 0,
      tickInterval: null,
    });
  }
  return servers.get(uuid)!;
}

function pushLog(uuid: string, line: string) {
  let buf = logBuffers.get(uuid);
  if (!buf) {
    buf = [];
    logBuffers.set(uuid, buf);
  }
  buf.push(line);
  if (buf.length > LOG_CAP) buf.shift();
  emit(uuid, line);
}

function emit(uuid: string, line: string) {
  const listeners = consoleListeners.get(uuid);
  if (!listeners) return;
  for (const cb of Array.from(listeners)) cb(line);
}

function ts() {
  const d = new Date();
  const hh = String(d.getHours()).padStart(2, "0");
  const mm = String(d.getMinutes()).padStart(2, "0");
  const ss = String(d.getSeconds()).padStart(2, "0");
  return `[${hh}:${mm}:${ss}`;
}

function log(uuid: string, level: "INFO" | "WARN" | "ERROR", msg: string) {
  pushLog(uuid, `${ts()} ${level}]: ${msg}`);
}

function delay(ms: number) {
  return new Promise<void>((r) => setTimeout(r, ms));
}

